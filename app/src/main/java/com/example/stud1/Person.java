package com.example.stud1;
//class describing data structure
public class Person {
    private int id;
    private String name;
    private String surname;
    private String fathername;
    private String age;

    public Person(int id, String name, String surname, String fathername, String age){
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.fathername = fathername;
        this.age = age;
    }


}
